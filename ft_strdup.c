#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*newstr;
	int		len;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	newstr = (char *) malloc(len + 1);
	if (newstr == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		newstr[i] = s1[i];
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
